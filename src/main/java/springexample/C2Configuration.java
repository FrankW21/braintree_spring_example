package springexample;

public class C2Configuration
{
    private String api;
    private String hpp;
    private String merchantAlias;
    private String secretKey;
    private String merchantSite;
    private String publicApiKey;

    public String getApi()
    {
        return api;
    }

    public void setApi(String api)
    {
        this.api = api;
    }

    public String getHpp()
    {
        return hpp;
    }

    public void setHpp(String hpp)
    {
        this.hpp = hpp;
    }

    public String getMerchantAlias()
    {
        return merchantAlias;
    }

    public void setMerchantAlias(String merchantAlias)
    {
        this.merchantAlias = merchantAlias;
    }

    public String getSecretKey()
    {
        return secretKey;
    }

    public void setSecretKey(String secretKey)
    {
        this.secretKey = secretKey;
    }

    public String getMerchantSite()
    {
        return merchantSite;
    }

    public void setMerchantSite(String merchantSite)
    {
        this.merchantSite = merchantSite;
    }

    public String getPublicApiKey()
    {
        return publicApiKey;
    }

    public void setPublicApiKey(String publicApiKey)
    {
        this.publicApiKey = publicApiKey;
    }
}
